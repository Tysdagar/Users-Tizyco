/**
 * Enumeration of error types used to categorize specific error scenarios.
 */
export enum ErrorTypes {
  /**
   * Represents errors that occur due to validation failures.
   */
  VALIDATION_ERROR = 'VALIDATION_ERROR',

  /**
   * Represents errors that occur when an operation is not valid or allowed.
   */
  NO_VALID_OPERATION_ERROR = 'NO_VALID_OPERATION_ERROR',
}
